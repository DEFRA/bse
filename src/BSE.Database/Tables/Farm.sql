    CONSTRAINT [CK_Farm_MapReference] CHECK ([MapReference] like '[A-Z][A-Z][0-9][0-9][0-9][0-9][0-9][0-9]'),
    CONSTRAINT [CK_Farm_NumericHerdmark1] CHECK ([NumericHerdmark1] like '[0-9][0-9][0-9][0-9][0-9][0-9]'),
    CONSTRAINT [CK_Farm_NumericHerdmark2] CHECK ([NumericHerdmark2] like '[0-9][0-9][0-9][0-9][0-9][0-9]'),
    CONSTRAINT [CK_Farm_Parish] CHECK ([IsNonGBFarm] = 1 and [Parish] is null or [IsNonGBFarm] = 0 and [Parish] is not null),
    CONSTRAINT [FK_Farm_luAHO] FOREIGN KEY ([AHO]) REFERENCES [dbo].[luAHO] ([Code]),
    CONSTRAINT [FK_Farm_luBSECounty] FOREIGN KEY ([County]) REFERENCES [dbo].[luBSECounty] ([Code]),
    CONSTRAINT [FK_Farm_luHerdType] FOREIGN KEY ([HerdType]) REFERENCES [dbo].[luHerdType] ([Code]),
    CONSTRAINT [FK_Farm_luPedigree] FOREIGN KEY ([PedigreeType]) REFERENCES [dbo].[luPedigreeType] ([Code])
);

